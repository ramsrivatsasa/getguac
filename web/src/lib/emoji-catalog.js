const ALL_EMOJIS = (() => {
  const seen = new Set()
  for (const s of EMOJI_SECTIONS) for (const e of s.emojis) seen.add(e)
  return [...seen]
})()

// Substring match on the emoji itself, the section label, or the tag list.
// Returns up to `limit` results, ranked: section-label match > tag match.
export function searchEmoji(query, limit = 64) {
  const q = (query || '').trim().toLowerCase()
  if (!q) return ALL_EMOJIS.slice(0, limit)

  const sectionHits = []
  for (const s of EMOJI_SECTIONS) {
    if (s.label.toLowerCase().includes(q) || s.id.includes(q)) {
      for (const e of s.emojis) sectionHits.push(e)
    }
  }
  const tagHits = []
  for (const [emoji, tags] of Object.entries(EMOJI_TAGS)) {
    if (tags.some(t => t.includes(q))) tagHits.push(emoji)
  }
  const merged = []
  const seen = new Set()
  for (const e of [...sectionHits, ...tagHits]) {
    if (!seen.has(e)) { merged.push(e); seen.add(e) }
    if (merged.length >= limit) break
  }
  return merged
}
